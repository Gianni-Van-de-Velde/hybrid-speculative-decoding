    @classmethod
    def from_pretrained(
            cls,
            use_eagle3=True,
            base_model_path=None,
            ea_model_path=None,
            total_token=60,
            depth=7,
            top_k=10,
            threshold=1.0,
            **kwargs,
    ):
        # assert Type=="LLaMA" or "Mixtral"
        Type = AutoConfig.from_pretrained(base_model_path).architectures[0]

        if Type == 'LlamaForCausalLM':
            base_model = KVLlamaForCausalLM.from_pretrained(
                base_model_path, **kwargs
            )
        elif Type == 'Qwen2ForCausalLM':
            base_model = KVQwen2ForCausalLM.from_pretrained(
                base_model_path, **kwargs
            )
        elif Type == 'Qwen3ForCausalLM':
            base_model = KVQwen3ForCausalLM.from_pretrained(
                base_model_path, **kwargs
            )
        else:
            base_model = KVMixtralForCausalLM.from_pretrained(
                base_model_path, **kwargs
            )

        configpath = os.path.join(ea_model_path, "config.json")
        if not os.path.exists(configpath):
            configpath = hf_hub_download(ea_model_path, "config.json")

        try:
            load_model_path = os.path.join(ea_model_path, "pytorch_model.bin")
            if not os.path.exists(load_model_path):
                load_model_path = hf_hub_download(ea_model_path, "pytorch_model.bin")
            ea_layer_state_dict = torch.load(load_model_path,
                                             map_location=base_model.device)
        except:
            from safetensors.torch import load_file
            load_model_path = os.path.join(ea_model_path, "model.safetensors")
            if not os.path.exists(load_model_path):
                load_model_path = hf_hub_download(ea_model_path, "model.safetensors")
            ea_layer_state_dict = load_file(load_model_path)
        model = cls(
            use_eagle3,
            base_model,
            base_model_path,
            configpath,
            total_token,
            depth,
            top_k,
            threshold,
            ea_layer_state_dict
        )

        if total_token == -1:
            device = model.base_model.model.layers[0].self_attn.q_proj.weight.device
            cans = [40, 48, 50, 56, 60]
            x = [1, 1.05, 1.07, 1.1, 1.13]
            times = []

            for i in range(len(cans)):
                length = cans[i]
                input_ids = torch.randint(0, model.config.vocab_size - 200, (1, length)).to(device)
                torch.cuda.synchronize()
                start_time = time.time()
                for _ in range(20):
                    torch.cuda.synchronize()
                    with torch.no_grad():
                        outputs = model.base_model(input_ids)
                    torch.cuda.synchronize()
                torch.cuda.synchronize()
                end_time = time.time()
                times.append((end_time - start_time) / x[i])
            total_token = cans[times.index(min(times))]
            model.ea_layer.total_tokens = total_token - 1

        return model

    def forward(
            self,
            input_ids=None,
            attention_mask=None,
            past_key_values=None,
            output_orig=False,
            position_ids=None,
    ):

        with torch.inference_mode():
            # Pass input through the base model
            outputs = self.base_model.model(
                input_ids=input_ids,
                attention_mask=attention_mask,
                past_key_values=past_key_values,
                position_ids=position_ids,
            )
            if output_orig:
                orig = self.base_model.lm_head(outputs[0])
            hidden_states = outputs[0]

        if output_orig:
            return outputs, orig, hidden_states
        else:
            return outputs, hidden_states

    @torch.no_grad()
    def eagenerate(
            self,
            input_ids,
            temperature=0.0,
            top_p=0.0,
            top_k=0.0,
            max_new_tokens=512,
            max_length=2048,
            log=False,
            is_llama3=False,
            use_casd=True,
            disable_eagle=False,
            continuation_length: int = 10,
            nr_continuations: int = 2,
            minimal_match_length: int = 1,

    ):
        if is_llama3:
            stop_token_id = self.tokenizer.convert_tokens_to_ids("<|eot_id|>")

        if temperature > 1e-5:
            logits_processor = prepare_logits_processor(temperature=temperature, top_p=top_p, top_k=top_k)
        else:
            logits_processor = None
        # assert input_ids.shape[0] == 1, "Only support batch size 1 for now!!"
        # Avoid modifying the input_ids in-place

        padding = (torch.zeros(1, 1, dtype=torch.long) - 1).to(input_ids.device)
        input_ids = input_ids.clone()
        self.ea_layer.reset_kv()

        # Initialize the past key and value states
        if hasattr(self, "past_key_values"):
            past_key_values = self.past_key_values
            past_key_values_data = self.past_key_values_data
            current_length_data = self.current_length_data
            # Reset the past key and value states
            current_length_data.zero_()
        else:
            (
                past_key_values,
                past_key_values_data,
                current_length_data,
            ) = initialize_past_key_values(self.base_model,max_length=max_length)
            self.past_key_values = past_key_values
            self.past_key_values_data = past_key_values_data
            self.current_length_data = current_length_data

        # print(input_ids[0])
        # print(self.tokenizer.decode(input_ids[0]))

        input_len = input_ids.shape[1]
        reset_tree_mode(self)
        draft_tokens, retrieve_indices, tree_mask, tree_position_ids, logits, hidden_state, _ = initialize_tree(
            input_ids, self, past_key_values, logits_processor
        )
        new_token = 0
        # TODO make this magic number 10 again
        max_length = max_length - self.ea_layer.total_tokens - 200

        context_input_ids = input_ids[0]
        real_total_tokens = self.ea_layer.total_tokens

        prefix_tree = PrefixTree(continuation_length + 1)
        all_tokens = context_input_ids.tolist()
        prefix_tree.insert_incremental(all_tokens)
        for idx in range(max_length):
            # generated_ids = input_ids.cpu().numpy()[0, input_length:]
            # print(context_input_ids)
            # print(generated_ids)
            # with Timer("all"):
            self.base_model.model.tree_mask = tree_mask

            draft_tokens = draft_tokens.to(input_ids.device)

            # Target model forward, get logits
            logits, hidden_state_new, outputs = tree_decoding(
                self,
                draft_tokens,
                past_key_values,
                tree_position_ids,
                input_ids,
                retrieve_indices,
            )
            # retrieve_indices=tree_buffers["retrieve_indices"]
            # logits = logits[0, retrieve_indices]
            draft_tokens = torch.cat((draft_tokens, padding), dim=1)
            candidates = draft_tokens[0, retrieve_indices]
            # verification
            best_candidate, accept_length, sample_p = evaluate_posterior(
                logits, candidates, logits_processor
            )

            if use_casd:
                read_from_answer = True

                tokens = candidates[best_candidate][:accept_length + 1].tolist()
                if read_from_answer:
                    all_tokens.extend(tokens)
                    prefix_tree.insert_incremental(tokens)
                tokens.append(torch.argmax(sample_p).item())
                top_matches = prefix_tree.top_k_unique_matches(tokens, nr_continuations=nr_continuations, minimal_match_length=minimal_match_length)

                continuations = []
                for pos, _ in top_matches:
                    continuation = all_tokens[pos - 1 : pos + 10]
                    continuations.append(torch.tensor(continuation))

                self.ea_layer.total_tokens = real_total_tokens - continuation_length * len(continuations)

            # with Timer("update_inference_inputs"):
            input_ids, draft_tokens, retrieve_indices, tree_mask, tree_position_ids, new_token, hidden_state, _ = update_inference_inputs(
                input_ids,
                candidates,
                best_candidate,
                accept_length,
                retrieve_indices,
                logits_processor,
                new_token,
                past_key_values_data,
                current_length_data,
                self,
                hidden_state_new,
                sample_p,
                disable_eagle=disable_eagle,
            )
                
            if use_casd:

                # Accumulate all continuation data
                accumulated_tokens = []
                accumulated_indices = []
                draft_tokens_ = draft_tokens.cpu()
                start_index = draft_tokens.size(1)
                
                for continuation in continuations:
                    continuation = continuation.cpu()
                    extra_prediction = continuation

                    correct_sequence = torch.ones((len(retrieve_indices)), dtype=bool)
                    first_match = -1
                    match_length = 0
                    
                    for i, token in enumerate(continuation):
                        indices = retrieve_indices[:, i]
                        correct_tokens = torch.logical_and(draft_tokens_[0, indices] == token, indices != -1)
                        correct_sequence_new = torch.logical_and(correct_sequence, correct_tokens)
                        if not correct_sequence_new.any() or i+1 >= retrieve_indices.shape[1]:
                            first_match = torch.nonzero(correct_sequence)[0, 0]
                            match_length = i
                            break
                        correct_sequence = correct_sequence_new

                    nr_extra_tokens = len(extra_prediction) - match_length
                    
                    # Accumulate tokens
                    accumulated_tokens.append(extra_prediction[match_length:])
                    
                    # Accumulate indices without padding
                    new_indices = torch.cat([
                        retrieve_indices[first_match, :match_length],
                        torch.tensor(start_index + np.arange(0, nr_extra_tokens), dtype=retrieve_indices.dtype)
                    ])
                    accumulated_indices.append(new_indices)
                    
                    # Update start_index for next continuation
                    start_index += nr_extra_tokens

                # Add all accumulated tokens at once
                if accumulated_tokens:
                    all_new_tokens = torch.cat(accumulated_tokens, dim=0).unsqueeze(0).to(draft_tokens.device)
                    draft_tokens = torch.cat([draft_tokens, all_new_tokens], dim=1)

                # Pad everything once at the end
                if accumulated_indices:
                    # Find maximum length across all indices (existing + new)
                    max_spec_length = max([retrieve_indices.size(1)] + [len(idx) for idx in accumulated_indices])
                    
                    # Pad retrieve_indices if needed
                    if retrieve_indices.size(1) < max_spec_length:
                        padding_needed = max_spec_length - retrieve_indices.size(1)
                        retrieve_indices = torch.cat(
                            [retrieve_indices, torch.zeros((retrieve_indices.size(0), padding_needed), dtype=retrieve_indices.dtype) - 1],
                            dim=1
                        )
                    
                    # Pad all accumulated indices to same length
                    padded_indices = []
                    for indices in accumulated_indices:
                        padding_needed = max_spec_length - len(indices)
                        if padding_needed > 0:
                            indices = torch.cat([indices, torch.zeros(padding_needed, dtype=indices.dtype) - 1])
                        padded_indices.append(indices.unsqueeze(0))

                    all_new_indices = torch.cat(padded_indices, dim=0)
                    retrieve_indices = torch.cat([retrieve_indices, all_new_indices], dim=0)

                # Build tree structures once after all additions
                # This is a bit of a hacky way to calculate tree_mask, as I assume that mask[:, 0] will always be true and thus I can map all -1 to 0. This is particularly the case for the way EAGLE and CASD work.
                total_tokens = len(draft_tokens[0])
                tree_mask = torch.zeros((total_tokens, total_tokens), dtype=torch.bool)
                __retrieve_indices = retrieve_indices.detach().clone()
                __retrieve_indices[__retrieve_indices == -1] = 0
                row_mask = torch.zeros(__retrieve_indices.size(0), total_tokens, dtype=torch.bool, device=__retrieve_indices.device)
                row_mask.scatter_(1, __retrieve_indices, True)
                cooccur = row_mask[:, :, None] & row_mask[:, None, :]
                tree_mask = cooccur.any(dim=0)
                tree_mask.tril_()

                # Add the prediction to the tree
                tree_position_ids = torch.sum(tree_mask, dim=1, dtype=int).to(draft_tokens.device) - 1

                # Fix dimensions
                tree_mask = tree_mask.unsqueeze(0).unsqueeze(0)

            # print('decoded until now', self.tokenizer.decode(input_ids[0]))
            self.ea_layer.total_tokens = real_total_tokens

            if is_llama3:
                if stop_token_id in input_ids[0, input_len:].tolist():
                    break

            if self.tokenizer.eos_token_id in input_ids[0, input_len:].tolist():
                break
            if new_token > max_new_tokens:
                break
            if input_ids.shape[1] > max_length:
                break
        if not log:
            return input_ids
        else:
            return input_ids, new_token, idx

    @torch.no_grad()
    def naivegenerate(
            self,
            input_ids,
            temperature=0.0,
            top_p=0.0,
            top_k=0.0,
            max_new_tokens=512,
            max_length=2048,
            log=False,
            is_llama3=False,

    ):
        if is_llama3:
            stop_token_id = self.tokenizer.convert_tokens_to_ids("<|eot_id|>")


        if temperature > 1e-5:
            logits_processor = prepare_logits_processor(temperature=temperature, top_p=top_p, top_k=top_k)
        else:
            logits_processor = None
        # assert input_ids.shape[0] == 1, "Only support batch size 1 for now!!"
        # Avoid modifying the input_ids in-place

        padding = (torch.zeros(1, 1, dtype=torch.long) - 1).to(input_ids.device)
        input_ids = input_ids.clone()
        self.ea_layer.reset_kv()

        # Initialize the past key and value states
        if hasattr(self, "past_key_values"):
            past_key_values = self.past_key_values
            past_key_values_data = self.past_key_values_data
            current_length_data = self.current_length_data
            # Reset the past key and value states
            current_length_data.zero_()
        else:
            (
                past_key_values,
                past_key_values_data,
                current_length_data,
            ) = initialize_past_key_values(self.base_model,max_length=max_length)
            self.past_key_values = past_key_values
            self.past_key_values_data = past_key_values_data
            self.current_length_data = current_length_data

        input_len = input_ids.shape[1]
        reset_tree_mode(self)
        outputs = self.base_model(input_ids, past_key_values=past_key_values, use_cache=True)
        new_token = 0
        max_length = max_length - self.ea_layer.total_tokens - 10
        for idx in range(max_length):
            if logits_processor is not None:
                logits = outputs.logits[:, -1]
                logits = logits_processor(None, logits)
                probabilities = torch.nn.functional.softmax(logits, dim=-1)
                input_id = torch.multinomial(probabilities, 1)
            else:
                input_id = outputs.logits[:, -1:].argmax(dim=-1)
            outputs = self.base_model(input_id, use_cache=True, past_key_values=past_key_values)
            input_ids = torch.cat([input_ids, input_id], dim=-1)
            new_token += 1

            if is_llama3:
                if stop_token_id in input_ids[0, input_len:].tolist():
                    break

            if self.tokenizer.eos_token_id in input_ids[0, input_len:].tolist():
                break
            if new_token > max_new_tokens:
                break
            if input_ids.shape[1] > max_length:
                break
        if not log:
            return input_ids
        else:
            return input_ids, new_token, idx

